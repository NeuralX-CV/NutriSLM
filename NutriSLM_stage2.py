def retrieve_relevant_foods(user_query, user_diet, top_k=4):
    """Hybrid Retrieval: Embeds query -> Fetches 20 -> Filters by metadata -> Returns top K"""
    query_vector = embedder.encode([user_query], convert_to_numpy=True)

    # Over-fetch the top 20 mathematically similar items
    distances, indices = index.search(query_vector, top_k * 5)

    retrieved_contexts = []

    for idx in indices[0]:
        allowed_diets = FOOD_METADATA[idx]["allowed_diets"]

        # Metadata Hard Filter: Only keep it if it fits the user's diet
        if user_diet.lower() in allowed_diets:
            retrieved_contexts.append(FOOD_DB_RAW[idx])

            # Stop once we have enough valid foods
            if len(retrieved_contexts) == top_k:
                break

    return retrieved_contexts

