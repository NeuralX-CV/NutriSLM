def run_rag_inference(user_input: str, user_diet: str, max_new_tokens: int = 600) -> tuple:

    # 1. Fetch context USING the hybrid metadata filter
    retrieved_docs = retrieve_relevant_foods(user_input, user_diet=user_diet, top_k=4)

    if not retrieved_docs:
        context_string = "No perfectly matching foods found. Please suggest standard general advice."
    else:
        context_string = "\n".join([f"- {doc}" for doc in retrieved_docs])

    # 2. Build the prompt and FORCE the JSON bracket at the end
    prompt = (
        "<|im_start|>system\n"
        "You are NutriSLM, a precise nutrition advisor. "
        "Use ONLY the provided 'Food Database Context' to suggest foods. "
        "Create a daily, weekly, and monthly goal plan using these specific foods. "
        "Always respond in structured JSON format.<|im_end|>\n"
        f"<|im_start|>user\n"
        f"Food Database Context:\n{context_string}\n\n"
        f"User Request: {user_input}<|im_end|>\n"
        "<|im_start|>assistant\n{"
    )

    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)

    # --- THE FIX: Calculate the exact length of the prompt ---
    prompt_length = inputs.input_ids.shape[1]

    with torch.no_grad():
        output_ids = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=True,
            temperature=0.2, # Very low temperature for strict JSON formatting
            pad_token_id=tokenizer.eos_token_id,
        )

    # --- THE FIX: Only decode the new tokens the model generated ---
    generated_ids = output_ids[0][prompt_length:]
    generated_text = tokenizer.decode(generated_ids, skip_special_tokens=True).strip()

    # Re-attach the forcing bracket we left in the prompt
    # Re-attach the forcing bracket we left in the prompt
    # Re-attach the forcing bracket we left in the prompt
    full_output = "{" + generated_text

    # --- THE FIX: Mathematical Bracket Counting ---
    try:
        start_idx = full_output.find("{")
        if start_idx == -1:
            raise ValueError("No JSON found")

        brace_count = 0
        end_idx = -1

        # Parse through the text character by character
        for i, char in enumerate(full_output[start_idx:]):
            if char == '{':
                brace_count += 1
            elif char == '}':
                brace_count -= 1

            # When we hit 0, we have found the exact end of the first JSON block
            if brace_count == 0:
                end_idx = start_idx + i + 1
                break

        if end_idx != -1:
            clean_json_string = full_output[start_idx:end_idx]

            # Auto-fix the model's semicolon hallucination
            clean_json_string = clean_json_string.replace("} ;", "},").replace("] ;", "],")

            result_json = json.loads(clean_json_string)
        else:
            raise ValueError("Unclosed JSON brackets")

    except Exception as e:
        # Fallback if it absolutely fails
        result_json = {"raw_output": full_output, "error": str(e)}

    return result_json, context_string

import sqlite3
from datetime import datetime

DB_PATH = "nutri_tracker.db"

def init_db():
    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()
    c.executescript("""
        CREATE TABLE IF NOT EXISTS users (
            user_id   TEXT PRIMARY KEY,
            name      TEXT,
            region    TEXT,
            diet_type TEXT,
            created   TEXT
        );
        CREATE TABLE IF NOT EXISTS deficiencies (
            id          INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id     TEXT,
            deficiency  TEXT,
            severity    TEXT,
            logged_date TEXT
        );
        CREATE TABLE IF NOT EXISTS food_logs (
            id        INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id   TEXT,
            food_name TEXT,
            meal_time TEXT,
            log_date  TEXT
        );
        CREATE TABLE IF NOT EXISTS goals (
            id           INTEGER PRIMARY KEY AUTOINCREMENT,
            user_id      TEXT,
            goal_type    TEXT,
            goal_text    TEXT,
            target_date  TEXT,
            completed    INTEGER DEFAULT 0,
            created_date TEXT
        );
    """)
    conn.commit()
    conn.close()

def upsert_user(user_id, name, region, diet_type):
    conn = sqlite3.connect(DB_PATH)
    conn.execute("""
        INSERT OR REPLACE INTO users VALUES (?,?,?,?,?)
    """, (user_id, name, region, diet_type, datetime.now().isoformat()))
    conn.commit(); conn.close()

def log_food(user_id, food_name, meal_time="lunch"):
    conn = sqlite3.connect(DB_PATH)
    conn.execute("INSERT INTO food_logs (user_id,food_name,meal_time,log_date) VALUES (?,?,?,?)",
                 (user_id, food_name, meal_time, datetime.now().date().isoformat()))
    conn.commit(); conn.close()

def save_goals(user_id, plan: dict):
    conn = sqlite3.connect(DB_PATH)
    today = datetime.now().date().isoformat()
    for gtype, gtext in [
        ("daily",   str(plan.get("daily_plan", ""))),
        ("weekly",  plan.get("weekly_goal", "")),
        ("monthly", plan.get("monthly_milestone", "")),
    ]:
        conn.execute("""
            INSERT INTO goals (user_id,goal_type,goal_text,target_date,created_date)
            VALUES (?,?,?,?,?)
        """, (user_id, gtype, gtext, today, today))
    conn.commit(); conn.close()

def get_weekly_summary(user_id):
    conn = sqlite3.connect(DB_PATH)
    rows = conn.execute("""
        SELECT food_name, meal_time, log_date FROM food_logs
        WHERE user_id=? ORDER BY log_date DESC LIMIT 50
    """, (user_id,)).fetchall()
    conn.close()
    return rows

